#Варіант 3
import math


a = int(input('Вкажіть сторону основи пірамиди:'))
h = int(input('Вкажіть висоту піраміди:'))

S = ((4*a)/2 * (((a/(2*math.tan(180/4))))+math.sqrt((h**2)+(a/(2*math.tan(180/4)**2)))))
#Площа
V = ((1/3)*h*(a**2))
#Об'єм


print( "Полоща поверхні піраміди(округлено) = ", math.ceil(S))
print( "Об'єм піраміди = ", math.ceil(V))
